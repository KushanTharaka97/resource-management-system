    private void jTextField_usernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_usernameFocusLost
        //if the user name equals to user name or "" empty 
        //we will show it
        if(jTextField_username.getText().trim().equals("") ||
           jTextField_username.getText().trim().toLowerCase().equals("username")){
            
                jTextField_username.setText("   please enter username!");
                jTextField_username.setForeground(Color.red);
                Border jTextBorder_username=BorderFactory.createMatteBorder(3,3,3,3, Color.red);
                jTextField_username.setBorder(jTextBorder_username);
        }
        jLabel_user.setBorder(null);
    }//GEN-LAST:event_jTextField_usernameFocusLost

