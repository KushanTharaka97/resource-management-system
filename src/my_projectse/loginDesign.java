    private void jButton_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_loginActionPerformed
        
        PreparedStatement st;
        ResultSet rs;
        
        //get username and password
        String username=jTextField_username.getText().trim();
        String password=String.valueOf(jPasswordField.getPassword());
        
        //creating SELECT query foe the database checking
        String querry="SELECT * FROM AdminTable WHERE name=? AND pass=?";
        
        try {
            st=My_conn.getConnection().prepareStatement(querry);
            
            st.setString(1,username);
            st.setString(2,password);
            
            rs=st.executeQuery();
            
            if(rs.next()){
                //show a new form
                Menue01 form=new Menue01();
                form.setVisible(true);
                form.pack();
                form.setLocationRelativeTo(null);
                
                //close the current form
                this.dispose();
                
                
                
            }else{
               //error message 
               JOptionPane.showMessageDialog(null,"Invalid username/password","Login Error",2);
               
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(loginDesign.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton_loginActionPerformed

